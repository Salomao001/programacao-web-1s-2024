const somar = (a, b) => parseFloat(a) + parseFloat(b);
const subtrair = (a, b) => parseFloat(a) - parseFloat(b);
const multiplicar = (a, b) => parseFloat(a) * parseFloat(b);
const dividir = (a, b) => parseFloat(a) / parseFloat(b);

module.exports = {
  somar,
  subtrair,
  multiplicar,
  dividir,
};
